@bp.route("/")
@login_required
def index():
    return render_template("help/index.html", topics=TOPICS)


@bp.route("/<key>")
@login_required
def topic(key: str):
    t = next((x for x in TOPICS if x["key"] == key), None)
    if not t:
        return render_template("help/index.html", topics=TOPICS)
    return render_template("help/topic.html", topic=t, topics=TOPICS)
